names = ['Arthur' , 'Buster', 'Brain', 'Francine', 'Muffy', 'Binky']

for i in names: #For every object inside the list known as names
    print(f"Hello there, {i}") #Print out the statement, where 'i'contains the value of each name

for r in range(len(names)): #For every object that exists within the total number of items inside the list 'names'
    print(f"Good day, {names[r]}") # Print out the statement where the object inside the indicated numerical index of the list is called